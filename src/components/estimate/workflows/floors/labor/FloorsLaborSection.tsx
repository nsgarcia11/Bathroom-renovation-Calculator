'use client';

import React, {
  useMemo,
  useCallback,
  useState,
  useEffect,
  useRef,
} from 'react';
import { Card, CardContent } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Plus, Trash2, ChevronDown, ChevronRight } from 'lucide-react';
import { useEstimateWorkflowContext } from '@/contexts/EstimateWorkflowContext';
import { useContractorContext } from '@/contexts/ContractorContext';
import { FLOORS_LABOR_ITEMS, FLOORS_CONFIG, HEATED_FLOOR_CONFIG } from '@/lib/constants';
import {
  LaborItem as ContextLaborItem,
  FlatFeeItem as ContextFlatFeeItem,
} from '@/types/estimate';

type LaborItem = ContextLaborItem & {
  source?: string;
  isAutoGenerated?: boolean;
};
type FlatFeeItem = ContextFlatFeeItem & {
  source?: string;
  isAutoGenerated?: boolean;
};

interface FloorsDesignData {
  width: string;
  length: string;
  extraMeasurements: Array<{
    id: number;
    label: string;
    width: string;
    length: string;
  }>;
  clientSuppliesTiles: boolean;
  selectedTileSizeOption: string;
  tileSize: { width: string; length: string };
  tilePattern: string;
  customPattern: string;
  isHeatedFloor: boolean;
  heatedFloorType: string;
  customHeatedFloorName: string;
  selectedPrepTasks: string[];
  plywoodThickness: string;
  joistCount: number;
  [key: string]: unknown;
}

export default function FloorsLaborSection() {
  const {
    getDesignData,
    getLaborItems,
    getFlatFeeItems,
    setLaborItems,
    setFlatFeeItems,
    isReloading,
  } = useEstimateWorkflowContext();

  const designData = getDesignData('floors') as FloorsDesignData | null;
  const design = useMemo(
    () =>
      designData || {
        width: '',
        length: '',
        extraMeasurements: [],
        clientSuppliesTiles: false,
        selectedTileSizeOption: 'select_option',
        tileSize: { width: '', length: '' },
        tilePattern: 'select_option',
        customPattern: '',
        isHeatedFloor: false,
        heatedFloorType: 'schluter',
        customHeatedFloorName: '',
        selectedPrepTasks: [],
        plywoodThickness: '3/4',
        joistCount: 1,
      },
    [designData]
  );

  const { hourlyRate: contractorHourlyRate } = useContractorContext();
  const contextLaborItems = getLaborItems('floors');
  const contextFlatFeeItems = getFlatFeeItems('floors');
  const [localLaborItems, setLocalLaborItems] = useState<LaborItem[]>([]);
  const [localFlatFeeItems, setLocalFlatFeeItems] = useState<FlatFeeItem[]>([]);
  const [isDesignOpen, setIsDesignOpen] = useState(true);
  const [isHeatedFloorOpen, setIsHeatedFloorOpen] = useState(true);
  const [isConstructionOpen, setIsConstructionOpen] = useState(true);
  const isUserActionRef = useRef(false);
  const processedChoicesRef = useRef<string>('');

  const totalSqFt = useMemo(() => {
    let totalArea = 0;
    const mainWidth = parseFloat(design.width) || 0;
    const mainLength = parseFloat(design.length) || 0;
    if (mainWidth > 0 && mainLength > 0)
      totalArea += (mainWidth * mainLength) / 144;

    // Add safety check for extraMeasurements
    if (design.extraMeasurements && Array.isArray(design.extraMeasurements)) {
      design.extraMeasurements.forEach((m) => {
        const sideWidth = parseFloat(m.width) || 0;
        const sideLength = parseFloat(m.length) || 0;
        if (sideWidth > 0 && sideLength > 0)
          totalArea += (sideWidth * sideLength) / 144;
      });
    }

    return totalArea;
  }, [design.width, design.length, design.extraMeasurements]);

  useEffect(() => {
    setLocalLaborItems(contextLaborItems);
    setLocalFlatFeeItems(contextFlatFeeItems);
  }, [contextLaborItems, contextFlatFeeItems]);

  // Helper function to get Ditra labor hours based on floor area bands
  const getDitraLaborHours = useCallback((sqFt: number): number => {
    for (const band of FLOORS_CONFIG.ditraLaborHoursBands) {
      if (sqFt <= band.maxSqFt) {
        return band.hours;
      }
    }
    return 7.5; // Default for very large areas
  }, []);

  // Helper function to get heated floor mat labor hours based on floor area bands
  const getHeatedFloorMatLaborHours = useCallback((sqFt: number): number => {
    for (const band of HEATED_FLOOR_CONFIG.matLaborHoursBands) {
      if (sqFt >= band.minSqFt && sqFt <= band.maxSqFt) {
        return band.hours;
      }
    }
    return 3.0; // Default minimum for very small floors
  }, []);

  const generateLaborItems = useCallback(() => {
    const laborItems: LaborItem[] = [];
    const flatFeeItems: FlatFeeItem[] = [];
    const laborItemsMap = FLOORS_LABOR_ITEMS(contractorHourlyRate);

    // Get multipliers based on tile size and pattern
    const tileSize = design.selectedTileSizeOption || 'custom';
    const tilePattern = design.tilePattern || 'stacked';

    const sizeMultiplier = FLOORS_CONFIG.sizeMultipliers[tileSize] || FLOORS_CONFIG.sizeMultipliers.custom;
    const patternMultiplier = FLOORS_CONFIG.patternMultipliers[tilePattern] || FLOORS_CONFIG.patternMultipliers.other;

    if (totalSqFt > 0) {
      // Tile Setting Hours: (TotalAreaSqFt / BaseTileProductivitySqFtPerHr) * SizeMultiplier * PatternMultiplier
      const tileHours = (
        (totalSqFt / FLOORS_CONFIG.baseTileProductivitySqFtPerHr) *
        sizeMultiplier *
        patternMultiplier
      ).toFixed(2);
      laborItems.push({
        ...laborItemsMap.tileFloor,
        id: `lab-floor-tile-${Date.now()}`,
        hours: tileHours,
        isAutoGenerated: true,
      });

      // Grout Hours: (TotalAreaSqFt / BaseGroutProductivitySqFtPerHr) * SizeMultiplier * PatternMultiplier
      const groutHours = (
        (totalSqFt / FLOORS_CONFIG.baseGroutProductivitySqFtPerHr) *
        sizeMultiplier *
        patternMultiplier
      ).toFixed(2);
      laborItems.push({
        ...laborItemsMap.groutFloor,
        id: `lab-floor-grout-${Date.now()}`,
        hours: groutHours,
        isAutoGenerated: true,
      });
    }

    // Add safety check for selectedPrepTasks
    if (design.selectedPrepTasks && Array.isArray(design.selectedPrepTasks)) {
      design.selectedPrepTasks.forEach((task) => {
        let taskItem: LaborItem | null = null;
        switch (task) {
          case 'self_leveling':
            // Self-leveling: Flat 2.25 hr per bathroom
            taskItem = {
              ...laborItemsMap.selfLeveling,
              id: `lab-floor-leveling-${Date.now()}`,
              hours: '2.25',
              isAutoGenerated: true,
            };
            break;
          case 'ditra':
            // Ditra: Hours based on floor area bands
            const ditraHours = getDitraLaborHours(totalSqFt).toFixed(2);
            taskItem = {
              ...laborItemsMap.installDitra,
              id: `lab-floor-ditra-${Date.now()}`,
              hours: ditraHours,
              isAutoGenerated: true,
            };
            break;
          case 'ditra_xl':
            // Ditra XL: Same hours as Ditra (area bands)
            const ditraXLHours = getDitraLaborHours(totalSqFt).toFixed(2);
            taskItem = {
              ...laborItemsMap.installDitraXL,
              id: `lab-floor-ditra-xl-${Date.now()}`,
              hours: ditraXLHours,
              isAutoGenerated: true,
            };
            break;
          case 'add_plywood':
            // Add plywood: Flat 2.75 hr per bathroom
            taskItem = {
              ...laborItemsMap.installPlywood,
              id: `lab-floor-plywood-${Date.now()}`,
              name: `Add ${design.plywoodThickness}" plywood for subfloor support`,
              hours: '2.75',
              rate: contractorHourlyRate.toString(),
              scope: 'construction',
              source: 'auto',
              isAutoGenerated: true,
            };
            break;
          case 'repair_subfloor':
            // Repair subfloor: 3 hr per patch
            taskItem = {
              ...laborItemsMap.repairSubfloor,
              id: `lab-floor-repair-subfloor-${Date.now()}`,
              hours: '3',
              isAutoGenerated: true,
            };
            break;
          case 'repair_joist':
            // Repair joist: 4.5 hr per joist Ã— joistCount
            const joistCount = design.joistCount || 1;
            const joistHours = (4.5 * joistCount).toFixed(2);
            taskItem = {
              ...laborItemsMap.repairJoist,
              id: `lab-floor-repair-joist-${Date.now()}`,
              name: `Repair floor joist${joistCount > 1 ? ` (${joistCount} joists)` : ''}`,
              hours: joistHours,
              isAutoGenerated: true,
            };
            break;
        }
        if (taskItem) laborItems.push(taskItem);
      });
    }

    // Heated floor labor - using new business logic
    if (design.isHeatedFloor && totalSqFt > 0) {
      // Calculate heated area: FloorArea * 90% coverage factor - toilet clearance
      const heatedAreaRaw = totalSqFt * HEATED_FLOOR_CONFIG.heatedCoverageFactor;
      const heatedAreaSqFt = Math.max(0, heatedAreaRaw - HEATED_FLOOR_CONFIG.toiletClearanceSqFt);

      // Get mat labor hours based on floor area bands
      const matLaborHours = getHeatedFloorMatLaborHours(totalSqFt);

      if (design.heatedFloorType === 'schluter') {
        // DITRA-HEAT Labor
        // 1. Mat installation - based on area bands
        laborItems.push({
          ...laborItemsMap.installDitraHeatMat,
          id: `lab-floor-ditra-heat-mat-${Date.now()}`,
          hours: matLaborHours.toFixed(2),
          isAutoGenerated: true,
        });

        // 2. Cable installation - HeatedAreaSqFt / productivity rate
        const cableLaborHours = heatedAreaSqFt / HEATED_FLOOR_CONFIG.productivity.ditraCableSqFtPerHr;
        laborItems.push({
          ...laborItemsMap.installDitraHeatCable,
          id: `lab-floor-ditra-heat-cable-${Date.now()}`,
          hours: cableLaborHours.toFixed(2),
          isAutoGenerated: true,
        });

        // 3. Electrical permit - $100 flat fee, 0 hours
        flatFeeItems.push({
          id: `flat-floor-ditra-heat-permit-${Date.now()}`,
          name: 'Electrical Permit (DITRA-HEAT)',
          quantity: '1',
          unitPrice: HEATED_FLOOR_CONFIG.electricalPermitFee.toString(),
          scope: 'heated_floor',
          source: 'auto',
          isAutoGenerated: true,
        } as FlatFeeItem);

      } else if (design.heatedFloorType === 'nuheat') {
        // NuHeat Labor
        // 1. Mat installation - based on area bands
        laborItems.push({
          ...laborItemsMap.installNuheatMat,
          id: `lab-floor-nuheat-mat-${Date.now()}`,
          hours: matLaborHours.toFixed(2),
          isAutoGenerated: true,
        });

        // 2. Cable installation - HeatedAreaSqFt / productivity rate
        const cableLaborHours = heatedAreaSqFt / HEATED_FLOOR_CONFIG.productivity.nuheatCableSqFtPerHr;
        laborItems.push({
          ...laborItemsMap.installNuheatCable,
          id: `lab-floor-nuheat-cable-${Date.now()}`,
          hours: cableLaborHours.toFixed(2),
          isAutoGenerated: true,
        });

        // 3. Electrical permit - $100 flat fee, 0 hours
        flatFeeItems.push({
          id: `flat-floor-nuheat-permit-${Date.now()}`,
          name: 'Electrical Permit (NuHeat)',
          quantity: '1',
          unitPrice: HEATED_FLOOR_CONFIG.electricalPermitFee.toString(),
          scope: 'heated_floor',
          source: 'auto',
          isAutoGenerated: true,
        } as FlatFeeItem);

      } else if (design.heatedFloorType === 'custom') {
        // Custom heated floor - user will set hours
        const systemName = design.customHeatedFloorName || 'Custom Heated Floor System';
        laborItems.push({
          ...laborItemsMap.installCustomHeatedFloor,
          id: `lab-floor-custom-heated-${Date.now()}`,
          name: `Install ${systemName}`,
          hours: matLaborHours.toFixed(2), // Default to mat hours, user can adjust
          isAutoGenerated: true,
        });
      }
    }

    return { laborItems, flatFeeItems };
  }, [design, totalSqFt, contractorHourlyRate, getDitraLaborHours, getHeatedFloorMatLaborHours]);

  useEffect(() => {
    if (isUserActionRef.current || isReloading) return;
    const choicesKey = `${design.width}-${design.length}-${
      design.selectedPrepTasks ? design.selectedPrepTasks.join(',') : ''
    }-${design.isHeatedFloor}-${design.heatedFloorType}-${
      design.plywoodThickness
    }-${design.joistCount}-${design.selectedTileSizeOption}-${design.tilePattern}`;

    // If choices haven't changed, skip regeneration
    if (processedChoicesRef.current === choicesKey) return;

    // Generate new auto items based on current design
    const { laborItems: newAutoItems, flatFeeItems: newFlatFeeItems } = generateLaborItems();

    // Get existing custom items (items added by user, not auto-generated)
    const existingItems = (contextLaborItems || localLaborItems || []) as LaborItem[];
    const customItems = existingItems.filter(
      (item) => item.source === 'custom' ||
                item.id.startsWith('labor-design-') ||
                item.id.startsWith('labor-construction-') ||
                item.id.startsWith('labor-heated-')
    );

    // Combine custom items with new auto-generated items
    const updatedItems = [...customItems, ...newAutoItems];

    if (updatedItems.length > 0 || existingItems.length > 0) {
      // Update both local state and context for immediate UI update
      setLocalLaborItems(updatedItems);
      setLaborItems('floors', updatedItems);
    }

    // Also update flat fee items if there are heated floor permits
    if (newFlatFeeItems.length > 0) {
      setLocalFlatFeeItems(newFlatFeeItems);
      setFlatFeeItems('floors', newFlatFeeItems);
    }

    processedChoicesRef.current = choicesKey;
  }, [
    design,
    generateLaborItems,
    setLaborItems,
    setFlatFeeItems,
    contextLaborItems,
    localLaborItems,
    isReloading,
  ]);

  const handleAddDesignLaborItem = useCallback(() => {
    const newItem: LaborItem = {
      id: `labor-design-${Date.now()}-${Math.random()
        .toString(36)
        .substr(2, 9)}`,
      name: '',
      hours: '1',
      rate: contractorHourlyRate.toString(),
      scope: 'design',
      source: 'custom',
      isAutoGenerated: false,
    };
    isUserActionRef.current = true;
    setLocalLaborItems((prev) => [...prev, newItem]);
    setTimeout(() => {
      setLaborItems('floors', [...localLaborItems, newItem]);
      isUserActionRef.current = false;
    }, 100);
  }, [localLaborItems, setLaborItems, contractorHourlyRate]);

  const handleAddHeatedFloorLaborItem = useCallback(() => {
    const newItem: LaborItem = {
      id: `labor-heated-${Date.now()}-${Math.random()
        .toString(36)
        .substr(2, 9)}`,
      name: '',
      hours: '1',
      rate: contractorHourlyRate.toString(),
      scope: 'heated_floor',
      source: 'custom',
      isAutoGenerated: false,
    };
    isUserActionRef.current = true;
    setLocalLaborItems((prev) => [...prev, newItem]);
    setTimeout(() => {
      setLaborItems('floors', [...localLaborItems, newItem]);
      isUserActionRef.current = false;
    }, 100);
  }, [localLaborItems, setLaborItems, contractorHourlyRate]);

  const handleAddConstructionLaborItem = useCallback(() => {
    const newItem: LaborItem = {
      id: `labor-construction-${Date.now()}-${Math.random()
        .toString(36)
        .substr(2, 9)}`,
      name: '',
      hours: '1',
      rate: contractorHourlyRate.toString(),
      scope: 'construction',
      source: 'custom',
      isAutoGenerated: false,
    };
    isUserActionRef.current = true;
    setLocalLaborItems((prev) => [...prev, newItem]);
    setTimeout(() => {
      setLaborItems('floors', [...localLaborItems, newItem]);
      isUserActionRef.current = false;
    }, 100);
  }, [localLaborItems, setLaborItems, contractorHourlyRate]);

  const handleLaborItemChange = useCallback(
    (id: string, field: keyof LaborItem, value: string) => {
      isUserActionRef.current = true;
      setLocalLaborItems((prev) =>
        prev.map((item) =>
          item.id === id ? { ...item, [field]: value } : item
        )
      );
      setTimeout(() => {
        const updatedItems = localLaborItems.map((item) =>
          item.id === id ? { ...item, [field]: value } : item
        );
        setLaborItems('floors', updatedItems);
        isUserActionRef.current = false;
      }, 100);
    },
    [localLaborItems, setLaborItems]
  );

  const handleDeleteLaborItem = useCallback(
    (id: string) => {
      isUserActionRef.current = true;
      setLocalLaborItems((prev) => prev.filter((item) => item.id !== id));
      setTimeout(() => {
        const updatedItems = localLaborItems.filter((item) => item.id !== id);
        setLaborItems('floors', updatedItems);
        isUserActionRef.current = false;
      }, 100);
    },
    [localLaborItems, setLaborItems]
  );

  const handleAddFlatFeeItem = useCallback(() => {
    const newItem: LaborItem = {
      id: `flat-fee-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
      name: '',
      hours: '0',
      rate: '0',
      scope: 'construction',
      source: 'custom',
      isAutoGenerated: false,
    };
    isUserActionRef.current = true;
    setLocalFlatFeeItems((prev) => [
      ...prev,
      newItem as unknown as FlatFeeItem,
    ]);
    setTimeout(() => {
      setLaborItems('floors', [...localLaborItems, newItem]);
      isUserActionRef.current = false;
    }, 100);
  }, [localLaborItems, setLaborItems]);

  const handleFlatFeeItemChange = useCallback(
    (id: string, field: keyof FlatFeeItem, value: string) => {
      isUserActionRef.current = true;
      setLocalFlatFeeItems((prev) =>
        prev.map((item) =>
          item.id === id ? { ...item, [field]: value } : item
        )
      );
      setTimeout(() => {
        const updatedItems = localLaborItems.map((item) => {
          if (item.id === id) {
            const flatFeeItem = localFlatFeeItems.find((f) => f.id === id);
            if (flatFeeItem) {
              return {
                ...item,
                name: flatFeeItem.name,
                rate: flatFeeItem.unitPrice,
              };
            }
          }
          return item;
        });
        setLaborItems('floors', updatedItems);
        isUserActionRef.current = false;
      }, 100);
    },
    [localLaborItems, localFlatFeeItems, setLaborItems]
  );

  const handleDeleteFlatFeeItem = useCallback(
    (id: string) => {
      isUserActionRef.current = true;
      setLocalFlatFeeItems((prev) => prev.filter((item) => item.id !== id));
      setTimeout(() => {
        const updatedItems = localLaborItems.filter((item) => item.id !== id);
        setLaborItems('floors', updatedItems);
        isUserActionRef.current = false;
      }, 100);
    },
    [localLaborItems, setLaborItems]
  );

  const flatFeeItems = localFlatFeeItems;

  // Separate labor items by scope
  const designLaborItems = useMemo(() => {
    return localLaborItems.filter((item) => item.scope === 'design');
  }, [localLaborItems]);

  const heatedFloorLaborItems = useMemo(() => {
    return localLaborItems.filter(
      (item) =>
        item.scope === 'heated_floor' ||
        item.name.toLowerCase().includes('heated') ||
        item.name.toLowerCase().includes('thermostat') ||
        item.name.toLowerCase().includes('schluter-ditra-heat') ||
        item.name.toLowerCase().includes('nuheat')
    );
  }, [localLaborItems]);

  const constructionLaborItems = useMemo(() => {
    return localLaborItems.filter(
      (item) =>
        item.scope === 'construction' &&
        !item.name.toLowerCase().includes('heated') &&
        !item.name.toLowerCase().includes('thermostat') &&
        !item.name.toLowerCase().includes('schluter-ditra-heat') &&
        !item.name.toLowerCase().includes('nuheat')
    );
  }, [localLaborItems]);

  // Calculate totals for each category
  const designTotalHours = designLaborItems.reduce(
    (sum, item) => sum + (parseFloat(item.hours) || 0),
    0
  );
  const designLaborTotal = designLaborItems.reduce(
    (sum, item) =>
      sum + (parseFloat(item.hours) || 0) * (parseFloat(item.rate) || 0),
    0
  );

  const heatedFloorTotalHours = heatedFloorLaborItems.reduce(
    (sum, item) => sum + (parseFloat(item.hours) || 0),
    0
  );
  const heatedFloorLaborTotal = heatedFloorLaborItems.reduce(
    (sum, item) =>
      sum + (parseFloat(item.hours) || 0) * (parseFloat(item.rate) || 0),
    0
  );

  const constructionTotalHours = constructionLaborItems.reduce(
    (sum, item) => sum + (parseFloat(item.hours) || 0),
    0
  );
  const constructionLaborTotal = constructionLaborItems.reduce(
    (sum, item) =>
      sum + (parseFloat(item.hours) || 0) * (parseFloat(item.rate) || 0),
    0
  );

  const renderLaborItem = useCallback(
    (item: LaborItem) => {
      return (
        <div
          key={item.id}
          className={`p-3 rounded-lg border shadow-sm w-full bg-white border-slate-200`}
        >
          <div className='flex items-center gap-2 mb-2 w-full'>
            <Input
              type='text'
              value={item.name}
              onChange={(e) =>
                handleLaborItemChange(item.id, 'name', e.target.value)
              }
              placeholder='Labor Task'
              className={`border-b-2 border-t-0 border-l-0 border-r-0 bg-transparent border-blue-300 focus:border-blue-500 focus:outline-none w-44 sm:w-full`}
            />
            <Button
              onClick={() => handleDeleteLaborItem(item.id)}
              variant='ghost'
              size='sm'
              className='text-red-500 hover:text-red-700 p-1 h-auto flex-shrink-0'
            >
              <Trash2 size={16} />
            </Button>
          </div>
          <div className='grid grid-cols-2 gap-3'>
            <div className='w-full'>
              <label className='text-xs text-slate-500'>Hours</label>
              <Input
                type='number'
                value={item.hours}
                onChange={(e) =>
                  handleLaborItemChange(item.id, 'hours', e.target.value)
                }
                placeholder='0'
                className='text-center w-full border-blue-300 focus:border-blue-500'
              />
            </div>
            <div className='w-full'>
              <label className='text-xs text-slate-500'>Rate ($/hr)</label>
              <Input
                type='number'
                value={item.rate}
                onChange={(e) =>
                  handleLaborItemChange(item.id, 'rate', e.target.value)
                }
                placeholder='0'
                className='text-center w-full border-blue-300 focus:border-blue-500'
              />
            </div>
            <div className='col-span-2 w-full'>
              <label className='text-xs text-slate-500'>Total</label>
              <div className='w-full p-2 text-center font-semibold text-slate-800 bg-white rounded-md'>
                $
                {(
                  (parseFloat(item.hours) || 0) * (parseFloat(item.rate) || 0)
                ).toFixed(2)}
              </div>
            </div>
          </div>
        </div>
      );
    },
    [handleLaborItemChange, handleDeleteLaborItem]
  );

  const renderFlatFeeItem = useCallback(
    (item: FlatFeeItem) => {
      return (
        <div
          key={item.id}
          className={`p-3 rounded-lg border shadow-sm w-full bg-white border-slate-200`}
        >
          <div className='flex items-center gap-2 mb-2 w-full'>
            <Input
              type='text'
              value={item.name}
              onChange={(e) =>
                handleFlatFeeItemChange(item.id, 'name', e.target.value)
              }
              placeholder='Flat Fee Task'
              className={`border-b-2 border-t-0 border-l-0 border-r-0 bg-transparent border-blue-300 focus:border-blue-500 focus:outline-none w-44 sm:w-full`}
            />
            <Button
              onClick={() => handleDeleteFlatFeeItem(item.id)}
              variant='ghost'
              size='sm'
              className='text-red-500 hover:text-red-700 p-1 h-auto flex-shrink-0'
            >
              <Trash2 size={16} />
            </Button>
          </div>
          <div className='w-full'>
            <label className='text-xs text-slate-500'>Total ($)</label>
            <Input
              type='number'
              value={item.unitPrice}
              onChange={(e) =>
                handleFlatFeeItemChange(item.id, 'unitPrice', e.target.value)
              }
              placeholder='0.00'
              className='text-center w-full border-b-2 border-t-0 border-l-0 border-r-0 border-blue-500 focus:border-blue-500'
            />
          </div>
        </div>
      );
    },
    [handleFlatFeeItemChange, handleDeleteFlatFeeItem]
  );

  const flatFeeTotal = flatFeeItems.reduce(
    (sum, item) => sum + (parseFloat(item.unitPrice) || 0),
    0
  );

  // Calculate total hours
  const totalHours = useMemo(() => {
    return localLaborItems.reduce(
      (sum, item) => sum + (parseFloat(item.hours) || 0),
      0
    );
  }, [localLaborItems]);

  // Calculate total amount
  const totalAmount = useMemo(() => {
    return (
      designLaborTotal +
      heatedFloorLaborTotal +
      constructionLaborTotal +
      flatFeeTotal
    );
  }, [designLaborTotal, heatedFloorLaborTotal, constructionLaborTotal, flatFeeTotal]);

  return (
    <div className='space-y-5'>
      <div className='flex justify-between items-baseline'>
        <h2 className='text-2xl font-bold text-slate-800'>Labor</h2>
        {totalHours > 0 && (
          <div className='flex items-center gap-2 text-sm text-slate-600'>
            <span className='font-semibold text-xl text-slate-800'>
              {totalHours.toFixed(1)} hrs
            </span>
          </div>
        )}
        <div className='flex items-center gap-2 text-sm text-slate-600'>
          <span className='font-semibold text-xl text-blue-600'>
            ${totalAmount.toFixed(2)}
          </span>
        </div>
      </div>

      <div className='space-y-4'>
        {/* Design Labor Section */}
        <Card>
          <CardContent className='p-3 space-y-3'>
            <div
              className='flex justify-between items-center cursor-pointer'
              onClick={() => setIsDesignOpen(!isDesignOpen)}
            >
              <div className='flex items-center gap-2 justify-between w-full'>
                <div className='flex items-center gap-2'>
                  {isDesignOpen ? (
                    <ChevronDown className='h-4 w-4 text-slate-600' />
                  ) : (
                    <ChevronRight className='h-4 w-4 text-slate-600' />
                  )}
                  <h3 className='text-lg font-semibold text-slate-700'>
                    Design Labor
                  </h3>
                </div>
                <span className='text-sm text-slate-500'>
                  {designTotalHours.toFixed(1)} hrs
                </span>
                <p className='font-bold text-blue-600 text-sm'>
                  ${designLaborTotal.toFixed(2)}
                </p>
              </div>
            </div>
            {isDesignOpen && (
              <>
                {designLaborItems.length > 0 ? (
                  <div className='space-y-2'>
                    {designLaborItems.map(renderLaborItem)}
                  </div>
                ) : (
                  <p className='text-sm text-slate-500 text-center py-4'>
                    No design labor items added yet.
                  </p>
                )}
                <div className='flex justify-center'>
                  <Button
                    onClick={handleAddDesignLaborItem}
                    variant='default'
                    className='w-auto mt-2 flex items-center justify-center gap-2 py-2.5 px-4 text-white font-semibold border-blue-200'
                  >
                    <Plus size={16} />
                    Add Item
                  </Button>
                </div>
              </>
            )}
          </CardContent>
        </Card>

        {/* Heated Floor Labor Section */}
        <Card>
          <CardContent className='p-3 space-y-3'>
            <div
              className='flex justify-between items-center cursor-pointer'
              onClick={() => setIsHeatedFloorOpen(!isHeatedFloorOpen)}
            >
              <div className='flex items-center gap-2 justify-between w-full'>
                <div className='flex items-center gap-2'>
                  {isHeatedFloorOpen ? (
                    <ChevronDown className='h-4 w-4 text-slate-600' />
                  ) : (
                    <ChevronRight className='h-4 w-4 text-slate-600' />
                  )}
                  <h3 className='text-lg font-semibold text-slate-700'>
                    Heated Floor Labor
                  </h3>
                </div>
                <span className='text-sm text-slate-500'>
                  {heatedFloorTotalHours.toFixed(1)} hrs
                </span>
                <p className='font-bold text-blue-600 text-sm'>
                  ${heatedFloorLaborTotal.toFixed(2)}
                </p>
              </div>
            </div>
            {isHeatedFloorOpen && (
              <>
                {heatedFloorLaborItems.length > 0 ? (
                  <div className='space-y-2'>
                    {heatedFloorLaborItems.map(renderLaborItem)}
                  </div>
                ) : (
                  <p className='text-sm text-slate-500 text-center py-4'>
                    No heated floor labor items added yet.
                  </p>
                )}
                <div className='flex justify-center'>
                  <Button
                    onClick={handleAddHeatedFloorLaborItem}
                    variant='default'
                    className='w-auto mt-2 flex items-center justify-center gap-2 py-2.5 px-4 text-white font-semibold border-blue-200'
                  >
                    <Plus size={16} />
                    Add Item
                  </Button>
                </div>
              </>
            )}
          </CardContent>
        </Card>

        {/* Construction Labor Section */}
        <Card>
          <CardContent className='p-3 space-y-3'>
            <div
              className='flex justify-between items-center cursor-pointer'
              onClick={() => setIsConstructionOpen(!isConstructionOpen)}
            >
              <div className='flex items-center gap-2 justify-between w-full'>
                <div className='flex items-center gap-2'>
                  {isConstructionOpen ? (
                    <ChevronDown className='h-4 w-4 text-slate-600' />
                  ) : (
                    <ChevronRight className='h-4 w-4 text-slate-600' />
                  )}
                  <h3 className='text-lg font-semibold text-slate-700'>
                    Construction Labor
                  </h3>
                </div>
                <span className='text-sm text-slate-500'>
                  {constructionTotalHours.toFixed(1)} hrs
                </span>
                <p className='font-bold text-blue-600 text-sm'>
                  ${constructionLaborTotal.toFixed(2)}
                </p>
              </div>
            </div>
            {isConstructionOpen && (
              <>
                {constructionLaborItems.length > 0 ? (
                  <div className='space-y-2'>
                    {constructionLaborItems.map(renderLaborItem)}
                  </div>
                ) : (
                  <p className='text-sm text-slate-500 text-center py-4'>
                    No construction labor items added yet.
                  </p>
                )}
                <div className='flex justify-center'>
                  <Button
                    onClick={handleAddConstructionLaborItem}
                    variant='default'
                    className='w-auto mt-2 flex items-center justify-center gap-2 py-2.5 px-4 text-white font-semibold border-blue-200'
                  >
                    <Plus size={16} />
                    Add Item
                  </Button>
                </div>
              </>
            )}
          </CardContent>
        </Card>

        {/* Flat Fee Section (remains unchanged) */}
        {flatFeeItems.length > 0 && (
          <Card>
            <CardContent className='p-3 space-y-3'>
              <div className='flex justify-between items-center'>
                <h3 className='text-lg font-semibold text-slate-700'>
                  Flat Fee Items
                </h3>
                <p className='font-bold text-blue-600 text-sm'>
                  ${flatFeeTotal.toFixed(2)}
                </p>
              </div>
              <div className='space-y-2'>
                {flatFeeItems.map(renderFlatFeeItem)}
              </div>
              <div className='flex justify-center'>
                <Button
                  onClick={handleAddFlatFeeItem}
                  variant='default'
                  className='w-auto mt-2 flex items-center justify-center gap-2 py-2.5 px-4 text-white font-semibold border-blue-200'
                >
                  <Plus size={16} />
                  Add Item
                </Button>
              </div>
            </CardContent>
          </Card>
        )}
      </div>
    </div>
  );
}
